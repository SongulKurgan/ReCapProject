using DataAccess.Abstract;
using Entities.Concrete;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace DataAccess.Concrete.InMemory
{
    public class InMemoryCarDal : ICarDal
    {
        List<Car> _cars;
        public InMemoryCarDal()
        {
            _cars = new List<Car> {
                new Car{ CarId=1, BrandId=1, ColorId=1, ModelYear=2017, DailyPrice=515, Description="Manuel,Benzin,Konfor"},
                new Car{ CarId=2, BrandId=1, ColorId=2, ModelYear=2020, DailyPrice=667, Description="Manuel,Dizel,Ekonomi"},
                new Car{ CarId=3, BrandId=2, ColorId=1, ModelYear=2019, DailyPrice=804, Description="Manuel,Benzin,Kompakt"},
                new Car{ CarId=4, BrandId=3, ColorId=5, ModelYear=2020, DailyPrice=712, Description="Manuel,Dizel,Ekonomi"}
            };
        }
        public void Add(Car car)
        {
            _cars.Add(car);
        }

        public void Delete(Car car)
        {
            Car carToDelete = _cars.SingleOrDefault(c=>c.CarId==car.CarId);
            _cars.Remove(carToDelete);
        }

        public List<Car> GetAll()
        {
            return _cars;
        }

        public List<Car> GetAllByBrand(int brandId)
        {
            return _cars.Where(c => c.BrandId == brandId).ToList();
        }

        public List<Car> GetAllByColor(int colorId)
        {
            return _cars.Where(c => c.ColorId == colorId).ToList();
        }

        public void Update(Car car)
        {
            Car carToUpdate = _cars.SingleOrDefault(c => c.CarId == car.CarId);
            carToUpdate.BrandId = car.BrandId;
            carToUpdate.ColorId = car.ColorId;
            carToUpdate.ModelYear = car.ModelYear;
            carToUpdate.DailyPrice = car.DailyPrice;
            carToUpdate.Description = car.Description;
        }
    }
}
